public class ReverseInGroupsOfSizeK {
    public static void main(String[] args) {
        Node head = new Node(10);
        head.next = new Node(20);
        head.next.next = new Node(30);
        head.next.next.next = new Node(40);
        head.next.next.next.next = new Node(50);
        head.next.next.next.next.next = new Node(60);
        head.next.next.next.next.next.next = new Node(70);
        head.next.next.next.next.next.next.next = new Node(80);

        iterativeTraversal(head);
        head = reverseInGroupsOfSizeK(head, 3);
        iterativeTraversal(head);
    }

    public static Node reverseInGroupsOfSizeK(Node head, int k) {
        Node curr = head, prev = null, next = null;
        int count = 0;
        while (curr != null && count < k) {
            next = curr.next;
            curr.next = prev;
            prev = curr;
            curr = next;
            count++;
        }

        if (next != null) {
            Node restHead = reverseInGroupsOfSizeK(curr, k);
            head.next = restHead;
        }

        return prev;
    }

    public static void iterativeTraversal(Node head) {
        Node curr = head;
        while (curr != null) {
            System.out.print(curr.data + " -> ");
            curr = curr.next;
        }
        System.out.print("null");
        System.out.println();
    }
}